import SwiftUI

struct ButtonModifier: ViewModifier {
    
    var color : String
    
    func body(content: Content) -> some View {
        
        content.frame(minWidth: 0, maxWidth: .infinity)
            .frame(height: 20)
            .padding()
            .foregroundColor(.white)
            .font(.system(size: 14, weight: .bold))
            .background(Color(color))
            .clipShape(Capsule())
        
    }
}


